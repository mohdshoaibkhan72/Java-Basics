public class swapping {
    public static void main(String[] args) {
        // write a code for swapping a number with temp variable

        int a = 10;
        int b = 30;
        System.out.println("a value is " + a + " : " + "b value is " + b);

        int temp = 30;
        b = a;
        a = temp;
        System.out.println(a + " " + b);

    }
}
